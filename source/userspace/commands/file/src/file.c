#include <stdio.h>
#include <eva.h>

/**
 *
 */
int main(int argc, char *argv[])
{
	if (argc > 1)
	{
		const char *arg = argv[1];

		Fd f = Open(arg);
		int64_t lenght = Length(f);

		if (lenght >= 1024) println("file %s: %f KB", arg, lenght / 1024);

		else println("file %s: %f B", arg, lenght);

		Close(f);
	}

	return 0;
}
